
class casting{
  public static void main(String[] args) {
   // casting
    // implicit casting
    double price = 110.0;
    double finalprice = price + 18;
    System.out.println(finalprice);

    // explicit casting
    int p  = 100;
    int fp = p + (int)18.0;
    System.out.println(fp);
  }
}